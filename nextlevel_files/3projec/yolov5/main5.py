    nomal_sc = 16#노말시간은 보행자 신호있어서 원하는시간+6
    # 슬라이드쇼번호
    normal_idx = nomal_sc
    emergency_idx = em_sc
    #긴급은 따로 cnt변수로 실행 한번하고 cnt값을 초기화해야지 여러번 반복가능
    cnt = 0

    while True:
        if STATE=="emergency":

                src = glob.glob('C:\\dev\\3projec\\img\\gin*.jpg')
                print(f"긴급 : {emergency_idx}")


                img = cv2.imread(src[emergency_idx])

                if img is None:  # 이미지가 없는 경우
                    print('이미지없음')
                    break
                #이미지 리사이징
                resize_img = cv2.resize(img, dsize=(0, 0), fx=0.12, fy=0.1, interpolation=cv2.INTER_AREA)
                height, width = img.shape[:2]
                # 가로 ,세로
                mat = np.float32([[1, 0, 0], [0, 1, 1]])
                tran = cv2.warpAffine(resize_img, mat, (width, height))
                #위에 윈도우바를 없애기위해 풀스크린을 활용
                cv2.namedWindow('tran', cv2.WND_PROP_FULLSCREEN)
                cv2.setWindowProperty('tran', cv2.WND_PROP_FULLSCREEN, cv2.WINDOW_FULLSCREEN)

                cv2.imshow("tran", tran)
                if cv2.waitKey(1000) >= 0:  # 1초 동안 사진보여주는데 만약에 키보드 입력이 있으면 종료
                    cv2.destroyAllWindows()
                    break
                #슬라이드쇼 반복
                emergency_idx -= 1
                if emergency_idx < 0:
                    emergency_idx = em_sc
                    cnt += 1
                    #위의 행동이 4번 반복되면 상태를 다시 "nomal"로변경
                    if cnt >5 :
                        STATE = "normal"
                        cnt=0
            #긴급일때



